											<h5>캐나다</h5>
											<p>캐나다에서는 노인의 교육에 중점을 둔다고 합니다.

												정부에서 대학에 노인 교육을 위해 보조금을 지급해서 대학에서 역사나 음악, 컴퓨터 등 다양한 강의를 무료로 들으며 새로운 배움을 즐긴다고 합니다. 

												제가 얼마전에 글을 보는데 한글을 배우는 어머님들에 대한 글이었어요.
												나이가 들어도 배움에 대한 열정은 여전하며 오히려 새로운 것에 대한 도전을 즐기시는 모습이 정말 아름답더라구요.</p><br />

											<h5>일본</h5>
											<p>고령화 사회가 된 일본,60세 이상 고령층에게 다양한 종류의 일자리를 제공하는 ‘실버인재센터’ 라는 것을 운영한다고 합니다.

												일자리를 알선받고, 다양한 취업교육을 받으며 또 다른 취미와 적성을 찾는다고 합니다.
												고령화사회가 되면서 노인들도 예전보다 훨씬 젊고, 활동량이 많습니다.
												돈을 벌고 경제활동을 하고 싶어하는 분들도 많죠.

												실제로 일본에서 시행되고 있는 이 제도도 일자리를 통해 삶의 의욕을 느끼게 하며 개인에겐 생활에 활력은 물론 경제적 능력까지 생길 수 있게 돕고 지역사회도 침체되지 않고 발전될 수 있게 하는 효과도 있다고 합니다.</p><br />

											<h5>미국</h5>
											<p>노인촌락이라는 노인 전용 아파트가 있다고 해요. 말하자면 실버타운 같은 개념인 것 같습니다. 55세 이상의 은퇴하신 분들이 지낼 수 있도록 정부차원에서 마련한 곳이라고 하는데요,

												노인의 신체조건을 고려해 거동이 불편하거나 여러 불편사항을 최소화한 설계가 돋보인다고 합니다.

												또한 또래들과 어울리며 여가 및 오락을 즐길 수도 있고 커뮤니티 활동도 할 수 있어서 사회성에도 좋을 것 같아요!</p><br />

											<h5>스웨덴</h5>
											<p>스웨덴은 복지로 유명한 나라 중 하나기도 하죠?

												자택에서 편하게 생활할 수 있도록 가정 도우미 서비스, 의료 서비스 등의 케어를 제공하여 익숙한 집에서 생활할 수 있게 한다고 합니다.

												각종 생활 케어는 물론 말벗까지 해줘서 신체적, 정신적인 도움을 드리는 것인데요

