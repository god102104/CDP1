										<h3>겨울철 노인 3대 질환! 특히 조심할 것은?</h3>
										<div class="blog-text">
											<p>날씨가 추워지면 자꾸 쑤시고 아프다고 하시는 분들이 많으신데요,<br />
												정말 날씨가 건강에 영향을 미치게 되는 걸까요??<br /><br />

												겨울은 기온이 낮아지고 공기가 건조해지면서 그에 따른<br />
												여러가지 건강 관리가 특히나 요구되는데요<br /><br />

												나이가 들면 면역력 및 신체 기능 및 능력이 떨어지기 때문에<br />
												특히나 주의해야할 겨울철 노인 3대 질환을 알려드립니다.</p><br />

											<h5>첫째, 호흡기 질환</h5>
											<p>그 중 하나는 바로 호흡기 질환인데요. 겨울에는 특히 감기는 물론 독감, 기관지염, 폐렴 등 다양한 호흡기 질환이 증가합니다. 겨울철의 온도와 습도는 폐나 기관지에 염증을 일으키는 세균과 바이러스를 더 번식하기 쉽고 좋게 만들어줍니다. 뿐만 아니라, 겨울철의 추운 공기는 기관지 점막의 방어능력을 낮춰 더욱 더 취약하게 만들어 더 호흡기 질환에 잘 걸릴 수 있습니다.</p>

											<p>겨울철에는 특히 감기로 오인을 해서 쉽게 넘어가다 더 큰 질환으로 번질 수 있기 때문에 단순한 감기 증상이라고 해도 꼭 병원을 찾는 것을 추천합니다.</p>
											<p>겨울철 노인 건강 관리를 위한 방법은 우선 외출 시 마스크를 꼭 챙기는 것을 추천합니다. 공기 중으로 바이러스가 퍼지기 쉽기 때문에 마스크로 그러한 번식을 막고 또한 요즘은 미세먼지도 심하기 때문에 외출 시에는 꼭 마스크를 착용하는 것을 추천합니다.</p>
											<p>또한 기관지가 건조해지면 방어능력도 낮아지기 때문에, 실내 공기가 건조해지지 않도록 실내에 젖은 수건을 두거나 가습기를 두는 등 습도 조절이 가능한 것을 두는 것을 추천합니다.</p>
											<p>또 앞서 말했듯이 단순한 감기로 느껴진다고 하더라도 병원에 방문하여 정확한 진단을 받고, 적절한 치료 및 관리를 하는 것이 필수입니다.</p><br />

											<h5>둘째, 심혈관 질환</h5>
											<p>겨울철에는 노인 사망률이 평소보다 높아지는데요. 겨울철에는 심근경색 사망률이 10% 증가, 뇌졸중 사망은 20%나 증가한다고 합니다.</p>

											<p>이 둘은 심혈관질환으로 묶어서 분리를 하는데요, 주로 원인은 고혈압, 당뇨병, 이상지질현증, 흡연과 비만 등이 원인이라고 합니다. 특히 겨울철에 더 사망률이 증가하는 이유는 추운 날씨에 체온을 유지하기 위해 혈압이 올라가고 심장이 열을 내기 위해 평소보다 더 일을 하면서 문제가 생기기 때문인데요.</p>
											<p>겨울철은 활동량은 줄고, 음주는 늘고 또 추운 날씨에 몸이 적응을 하지 못하여 그렇다는 등의 다양한 가설이 있다고 합니다.</p>

											<p>겨울철 심혈관 질환은 어떻게 예방을 할까요? 특히나 점점 추워지고 한파가 심해진다는 올해는 더욱 유의해야할 것 같은데요. 실내온도는 18도 이상으로 유지를 하고 잘 때도 난방기기를 잘 활용하는 것이 중요합니다. 체온이 낮아지지 않도록 관리를 하고 외출을 할 때도 모자, 목도리 등 보온 장비를 착용하여 체온을 유지해야합니다.</p>
											<p>또한 앞서 말했듯 고혈압, 당뇨 등의 위험요인이 있다면 평소보다 더 신경을 쓰는 것이 좋습니다. 꾸준한 질병 관리는 물론 적절한 운동과 건강한 식사습관으로 건강을 유지해야합니다.</p><br />

											<h5>셋째, 낙상 및 골절</h5>
											<p>겨울철에는 특히 낙상 및 골절을 주의해야 합니다. 겨울철엔 몸은 웅크리고 다니며 운동량이 부족해서 근육이 풀리지 않은 상태인 경우가 많습니다. 그리고 곳곳이 얼어 더욱 미끄러지기 좋은 환경인데요. 나이가 들면 골다공증에 걸리기가 쉽고 시력 및 균형감각 등도 약해져 더욱 크게 다칠 위험이 높습니다.</p>

											<p>특히 겨울철에 낙상으로 인한 부상은 허리나 하프 관절 부위 뼈가 부러져서 움직이기 힘든 정도로 다칠 경우가 많으며 이런 경우에는 여러 합병증이 높아짐은 물론 사망률도 높아지기 때문에 각별한 주의가 필요합니다.</p>
											<p>겨울철 낙상을 주의하는 방법은 역시 넘어지지 않는 것이 가장 기본입니다. 그러기 위해서는 특히 미끄러운 지역은 주의해야 하는데요, 미끄러움 방지 신발을 신는 것이 좋습니다. 그리고 빙판길 등을 걸을 때는 피하는 것이 좋습니다. 또한 특이한 점은 낙상 사고는 집에서도 많이 발생하기 때문에 욕실 등 물기가 있는 곳은 주의하며, 미끄럼 방지 매트 등을 까는 것이 좋습니다.</p>

											<p>그리고 골다공증이 있는 경우는 약 등 치료에 신경을 쓰는 것이 좋습니다. 또한 근력, 지구력, 유연성, 균형감각 등을 다지기 위해서 꾸준한 운동이 필요한데요. 걷기 등 간단한 운동은 물론 스트레칭으로 뭉친 근육을 풀어주는 것도 하나의 방법이라 할 수 있습니다.</p>
											<p>그리고 유제품 및 칼슘이 많은 음식을 섭취하여 칼슘을 섭취하고, 하루 15분 이상 햇빛을 쬐어서 비타민    D를 생성하는 것도 하나의 방법입니다.</p>
											
